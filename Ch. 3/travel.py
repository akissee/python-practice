# This program demonstrates lists and the different ways you can modify them

locations = ['iceland', 'scotland', 'alaska', 'canada']
print(locations)
print("\n")

print(sorted(locations))
print("\n")

print(locations)
print("\n")

print(sorted(locations, reverse=True))
print("\n")

print(locations)
print("\n")

locations.reverse()
print(locations)
print("\n")

locations.reverse()
print(locations)
print("\n")

locations.sort()
print(locations)
print("\n")

locations.sort(reverse=True)
print(locations)
print("\n")
